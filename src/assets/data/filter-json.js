// Create a map for quick lookup of game codes by game name
const gameCodeMap = new Map();
filterData.forEach(([code, name]) => {
    gameCodeMap.set(name, code);
});

// Process all objects - update gameCode for matching ones, leave others unchanged
const processedObjects = jsonData.map(obj => {
    if (obj && obj.gameName && gameCodeMap.has(obj.gameName)) {
        // Replace the gameCode with the one from our filter list
        const newGameCode = gameCodeMap.get(obj.gameName);
        return {
            ...obj,
            gameCode: newGameCode
        };
    }
    // Leave non-matching objects unchanged
    return obj;
});

